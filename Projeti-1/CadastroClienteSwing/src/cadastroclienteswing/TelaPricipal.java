    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
          int linhaSelecionada = tabelaClientes.getSelectedRow();
      if (linhaSelecionada >= 0) {    
        Long cpf = (Long) tabelaClientes.getValueAt(linhaSelecionada, 1); // Supondo que o CPF está na coluna 1
        String nome = txtNome.getText();
        String endereco = txtEnd.getText();
        String numero = txtNumero.getText();
        String cidade = txtCidade.getText();
        String estado = txtEstado.getText();
        String telefone = txtTelefone.getText();
        if (!isCamposValidos(nome, cpf.toString(), endereco, numero, cidade, estado, telefone)){
            JOptionPane.showMessageDialog(null, "Existem capos não preenchidos", "Atenção",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        Cliente cliente = new Cliente(nome, cpf.toString(), telefone, endereco, numero, cidade, estado);
        this.clienteDAO.alterar(cliente);
        
            modelo.removeRow(linhaSelecionada);
            modelo.addRow(new Object[]{cliente.getNome(), cliente.getCpf(), cliente.getEnd(), cliente.getNumero(), 
                cliente.getCidade(), cliente.getEstado(), cliente.getTel()});
            limparCampos();
        
       } else {
        JOptionPane.showMessageDialog(this, "Por favor, selecione um cliente para atualizar.", "Erro", JOptionPane.ERROR_MESSAGE);
    } 
    }//GEN-LAST:event_btnAtualizarActionPerformed

